    // ---- Parse a level map into game objects ----
    function parseLevel(map, themeKey, levelNum) {
        const theme = THEMES[themeKey];
        const rows = map.length;
        const cols = map[0].length;
        const data = {
            name: theme.name,
            num: levelNum,
            theme: theme,
            rows: rows,
            cols: cols,
            tiles: map,
            playerStart: { x: 0, y: 0 },
            portal: null,
            coins: [],
            enemies: [],
            checkpoints: [],
            powerups: [],
            movingPlatforms: []
        };

        const T = BG.T;
        for (let r = 0; r < rows; r++) {
            for (let c = 0; c < cols; c++) {
                const ch = map[r][c];
                const px = c * T;
                const py = r * T;
                switch (ch) {
                    case 'P': data.playerStart = { x: px, y: py }; break;
                    case '@': data.portal = { x: px, y: py, w: T, h: T }; break;
                    case '*': data.coins.push({ x: px + 8, y: py + 8, w: 16, h: 16, alive: true, bobOffset: Math.random() * Math.PI * 2 }); break;
                    case 'G': data.enemies.push({ type: 'slime', x: px, y: py, startX: px, range: 3 * T, dir: 1 }); break;
                    case 'F': data.enemies.push({ type: 'bat', x: px, y: py, startX: px, startY: py, range: 4 * T, dir: 1, phase: Math.random() * Math.PI * 2 }); break;
                    case 'S': data.enemies.push({ type: 'crab', x: px, y: py, startX: px, range: 2 * T, dir: 1 }); break;
                    case 'C': data.checkpoints.push({ x: px, y: py - T, w: T, h: T * 2, activated: false }); break;
                    case '+': data.powerups.push({ type: 'doublejump', x: px + 4, y: py + 4, w: 24, h: 24, alive: true }); break;
                    case '>': data.powerups.push({ type: 'speed', x: px + 4, y: py + 4, w: 24, h: 24, alive: true }); break;
                    case 'M': data.movingPlatforms.push({ x: px, y: py, w: T * 3, h: 12, startX: px, moveX: T * 4, speed: 1, dir: 1, vertical: false }); break;
                    case 'V': data.movingPlatforms.push({ x: px, y: py, w: T * 3, h: 12, startY: py, moveY: T * 3, speed: 1, dir: 1, vertical: true }); break;
                }
            }
        }

        // Clean the tile map - replace entity markers with air for collision
        data.cleanTiles = map.map(row =>
            row.replace(/[P@\*GFSCc\+\>MV]/g, '.')
        );

        return data;
    }

